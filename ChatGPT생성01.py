# Pros and Cons

# List
list_pros = ["Ordered", "Mutable", "Allows duplicates"]
list_cons = ["Slower for large data", "Not suitable for hash-based operations", "Takes more memory"]

# Tuple
tuple_pros = ["Ordered", "Immutable", "Faster access than lists"]
tuple_cons = ["Cannot be modified after creation", "Limited functionality compared to lists"]

# Set
set_pros = ["Unordered", "No duplicates", "Fast membership testing"]
set_cons = ["Cannot access elements by index", "Unpredictable order", "Limited operations compared to lists"]

# Dict
dict_pros = ["Key-Value pairs", "Fast lookup by key", "Flexible, can hold various data types as values"]
dict_cons = ["Unordered (Python 3.7+ maintains insertion order)", "Key uniqueness required", "Slightly slower than sets"]

# Printing pros and cons
print("List Pros:", list_pros)
print("List Cons:", list_cons)
print("\nTuple Pros:", tuple_pros)
print("Tuple Cons:", tuple_cons)
print("\nSet Pros:", set_pros)
print("Set Cons:", set_cons)
print("\nDict Pros:", dict_pros)
print("Dict Cons:", dict_cons)
